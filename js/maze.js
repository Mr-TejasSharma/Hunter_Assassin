function getRandomMaze() {
    let randomIndex = Math.floor(Math.random() * mazeWall.length);
    return mazeWall[randomIndex];
}

let randomMaze = getRandomMaze();

function renderMaze() {

    for (let i = 0; i < randomMaze.length; i++) {
        for (let j = 0; j < randomMaze[i].length; j++) {
            if (randomMaze[i][j] === '#') {
                //ctx.fillRect(j * cell, i * cell, cell, cell);

                if (wallImage.complete && wallImage.naturalWidth !== 0) {
                    ctx.drawImage(wallImage, j * cell, i * cell, cell, cell);
                }
                else {
                    ctx.fillStyle = "black";
                    ctx.fillRect(j * cell, i * cell, cell, cell);
                }
            }
            else if (randomMaze[i][j] === '.') {
                if (cellImage.complete && cellImage.naturalWidth !== 0) {
                    ctx.drawImage(cellImage, j * cell, i * cell, cell, cell);
                }
            }
        }
    }
};